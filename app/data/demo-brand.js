const creatorsById = new Map(demoCreators.map((creator) => [creator.id, creator]));
const productsByTitle = new Map(demoProducts.map((product) => [product.title, product]));

export function getDemoCreator(creatorId) {
  return creatorsById.get(creatorId) || null;
}

export function getDemoProductByTitle(title) {
  return productsByTitle.get(title) || null;
}

export function getDemoCreative(creativeId) {
  return demoCreatives.find((creative) => creative.id === creativeId) || null;
}

export function enrichDemoCreative(creative) {
  const creator = getDemoCreator(creative.creatorId) || {};
  const product = getDemoProductByTitle(creative.productTitle) || {};

  return {
    ...creative,
    creatorName: creator.name || creative.creatorName || "Demo Creator",
    creatorHandle: creator.handle || creative.creatorHandle || "",
    productHandle: creative.productHandle || product.handle || "",
  };
}

export function buildDemoPerformanceSeries(days = 30) {
  const today = new Date("2026-06-24T12:00:00.000Z");

  return Array.from({ length: days }, (_, index) => {
    const date = new Date(today);
    date.setUTCDate(today.getUTCDate() - (days - 1 - index));
    const momentum = 0.72 + index * 0.018;
    const creative = demoCreatives[index % demoCreatives.length];
    const views = Math.round((creative.views / 18) * momentum);
    const clicks = Math.round((creative.clicks / 18) * momentum);
    const orders = Math.max(1, Math.round((creative.orders / 18) * momentum));
    const revenue = Math.round((creative.revenue / 18) * momentum);
    const spend = Math.round((creative.spend / 18) * momentum);
    const roas = spend ? Number((revenue / spend).toFixed(2)) : 0;

    return {
      id: `demo-day-${date.toISOString().slice(0, 10)}`,
      creativeId: creative.id,
      sourceCreativeId: creative.id,
      sourcePlatform: "shopify_demo",
      date: date.toISOString().slice(0, 10),
      views,
      clicks,
      orders,
      conversions: orders,
      revenue,
      spend,
      roas,
      conversionRate: clicks ? Number(((orders / clicks) * 100).toFixed(2)) : 0,
      creativeScore: creative.score,
      likes: Math.round(views * (creative.engagementRate / 100) * 0.72),
      comments: Math.round(views * (creative.engagementRate / 100) * 0.08),
      shares: Math.round(views * (creative.engagementRate / 100) * 0.2),
      label: DEMO_PERFORMANCE_LABEL,
    };
  });
}

